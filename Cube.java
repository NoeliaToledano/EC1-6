public class Cube{
    private int[][] back;
    private int[][] down;
    private int[][] front;
    private int[][] left;
    private int[][] right;
    private int[][] up;

    Cube(int[][] back, int[][] down, int[][] front, int[][] left, int[][] right, int[][] up){
        this.back=back;
        this.down=down;
        this.front=front;
        this.left=left;
        this.right=right;
        this.up=up;

    }

}